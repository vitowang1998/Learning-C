#include <stdio.h>

int main(){
    // Addition
    printf("%d\n", 1 + 1);
    
    // Substraction
    printf("%d\n", 12 - 1);
    
    // Multiplication
    printf("%d\n", 12 * 3);
    
    // Division: integer division -- discard all decimal parts
    printf("%d\n", 12 / 3);
    printf("%d\n", 13 / 3);
    printf("%d\n", 14 / 3);
    
    return 0;
}
