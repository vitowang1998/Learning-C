// abs_int(n) returns the absolute value of an integer
// O(1)
int abs_int(int n) {
    if (n >= 0) {
        return n;
    } else {
        return -n;
    }
}

// abs_double(n) returns the absolute value of a double
// O(1)
double abs_double(double n) {
    if (n >= 0) {
        return n;
    } else {
        return -n;
    }
}
